    private void btnReplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReplyActionPerformed
        if (tblIssue.getSelectedRow() != -1){ // if an isssue is selected
            
            // retrieving necessary values
            String replyValue = tblIssue.getValueAt(tblIssue.getSelectedRow(), 5).toString();
            String IDValue = tblIssue.getValueAt(tblIssue.getSelectedRow(), 0).toString();
            if (!"".equals(txtReply.getText())){ // if user has entered the response
                FileOperations file = new FileOperations();
                String status = file.modify("CustomerIssue.txt", 5, replyValue, txtReply.getText(), IDValue , 0); // modifying the reply
                if ("Record updated successfully.".equals(status)){ // if modification successfull
                    int rowSelected = tblIssue.getSelectedRow();
                    updateTable(tblIssue, rowSelected, 5, txtReply.getText()); // updated row accordingly
                }
                else{
                    JOptionPane.showMessageDialog(null, status, "Information", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Please enter response in the provided Text Area.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Select an IssueId First.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnReplyActionPerformed

    // method to modify the status of issue
    private boolean modifyStatus(String newStatus, int selectedRow){
        // retrieving necessary values
        String oldStatus = tblIssue.getValueAt(selectedRow, 2).toString();
        String customerIssueID = tblIssue.getValueAt(selectedRow, 0).toString();
        FileOperations file = new FileOperations();
        String status = file.modify("CustomerIssue.txt", 2, oldStatus, newStatus, customerIssueID, 0);// modifying the selected issue
        if ("Record updated successfully.".equals(status)){ // if modification is successfull
            updateTable(tblIssue, selectedRow, 2, newStatus); // updating the table
            return true; // modification successful - returning true
        }
        else{
            return false;
        }
    }
    
    private void btnChangeStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeStatusActionPerformed
        int selectedRow = tblIssue.getSelectedRow();
        if (selectedRow != -1){ // if issue has been selected
            if (cboChangeStatus.getSelectedIndex() != -1){ // if status has been updated
                String newStatus = cboChangeStatus.getSelectedItem().toString();
                if(!modifyStatus(newStatus, selectedRow)){ // calling method to modify the status - if method returns false - modifiaction failed
                    JOptionPane.showMessageDialog(null, "Unsuccessfull attempt to update status.", "Information", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Please select a Status first.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Select One IssueId First.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnChangeStatusActionPerformed

    // method to check availability of the staff chosen 
    private boolean checkAvailabilityOfStaff(String selectedID, ArrayList<CustomerIssue> issues){
        boolean checked = true;
        for (CustomerIssue i : issues){
            if (i.getStaffID().equals(selectedID)){ // if staffID of cutomerIssue is equal to staffID selected
                  checked = false; // staff not available    
            }
        }
        return checked;
    }
    
