    // method to customer issues to rows in the table
    private void addRowsIssues(ArrayList<Casting> array){
        DefaultTableModel table = (DefaultTableModel) tblIssue.getModel();
        table.setRowCount(0);
        Casting cast = new Casting();
        ArrayList<Object> issuesObjects = cast.convertRecordsToObjects(array, "CustomerIssue");
        ArrayList<CustomerIssue> issues = new ArrayList();
        
        // adding CustomerIssue object to the ArrayList
        for (Object obj : issuesObjects) {
            if (obj instanceof CustomerIssue customerIssue) {
                issues.add(customerIssue);
            } 
            else {
                JOptionPane.showMessageDialog(null, "Unexpected object type: " + obj.getClass().getName(), "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
                
        for (CustomerIssue i : issues) {//looping through ArrayList of CustomerIssues
            table.addRow(new Object[]{ //adding rows to the table, accessing attributes using getters
                i.getIssueID(),
                i.getIssueDescription(),
                i.getStatus(),
                i.getStaffID(),
                i.getStaffName(),
                i.getResponse(),
                i.getBookingID(),
                i.getCustomerID()
            });
        } 
    }
    
