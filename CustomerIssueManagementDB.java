    // method to load scheduler objects from scheduler.txt
    private ArrayList<Scheduler> schedulerLoader(){
        ArrayList<Scheduler> schedulers = new ArrayList();
        File checkFile = new File("scheduler.txt");
        if (!checkFile.exists()) { //checking if the file exist first
            JOptionPane.showMessageDialog(null, "No scheduler has been added yet.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            FileOperations file = new FileOperations();
            ArrayList<Casting> schedulerRecords = file.readFile("scheduler.txt");
            if (!schedulerRecords.isEmpty()){// check if ArrayList is empty
                Casting cast = new Casting();
                ArrayList<Object> schedulerObjects = cast.convertRecordsToObjects(schedulerRecords, "Scheduler");

                // adding the scheduler objects to the scheduler ArrayList
                for (Object obj : schedulerObjects) {
                    if (obj instanceof Scheduler scheduler) {
                        schedulers.add(scheduler);
                    } 
                    else {
                        JOptionPane.showMessageDialog(null, "Unexpected object type: " + obj.getClass().getName(), "Information", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "No scheduler has been added yet.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        return schedulers;
    }
    
    
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        File checkFile = new File("CustomerIssue.txt");
        if (!checkFile.exists()) { // checking if the file exist first
            JOptionPane.showMessageDialog(null, "No Customer Issue has been raised until now.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            FileOperations file = new FileOperations();
            ArrayList<Casting> customerIssues = file.readFile("CustomerIssue.txt"); // file exits - thus reading form the file

            if (!customerIssues.isEmpty()){ // if the ArrayList is not empty - there are CustomerIssues in the file 
                addRowsIssues(customerIssues); // adding the issuse to the table
            }
            else{
                JOptionPane.showMessageDialog(null, "No Customer Issue has been raised until now.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        
        //loading the scheduler objects into the tblStaff
        ArrayList<Scheduler> schedulers = schedulerLoader();
        DefaultTableModel table = (DefaultTableModel) tblStaff.getModel();
        for (Scheduler i : schedulers) {
            table.addRow(new Object[]{
                i.getId(),
                i.getName()
            });
        }
    }//GEN-LAST:event_formWindowOpened

