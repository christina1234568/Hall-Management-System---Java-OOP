    private void btnAddStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStaffActionPerformed
        int selectedRow = tblStaff.getSelectedRow();
        if (selectedRow != -1){ // if a row has been selected
            String selectedStaffID = tblStaff.getValueAt(selectedRow, 0).toString();
            String selectedStaffName = tblStaff.getValueAt(selectedRow, 1).toString();
            
            Scheduler staff = new Scheduler(selectedStaffID, selectedStaffName);//checking if already added to arrayList
            if (schedulerAssigned.contains(staff)){
                JOptionPane.showMessageDialog(null, "This Staff has already been added to work on this project.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                File checkFile = new File("ConstructionProjects.txt");
                if (!checkFile.exists()) { //checking if file does not exist - if does not exist, no project contains the current staff object
                    schedulerAssigned.add(staff);
                }
                else{
                    if(checkAvailabilityOfStaff(staff)){ //if there is a file - checking for staff in other projects
                        schedulerAssigned.add(staff);
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Staff is working on another project. Please choose another Staff.", "Information", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Select a StaffID first.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAddStaffActionPerformed

