    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        if ("".equals(txtProjectName.getText())){ // validation for not entering project name
            JOptionPane.showMessageDialog(null, "Enter a Project Name.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtProjectName.requestFocusInWindow();
        }
        else if ("".equals(txtDescription.getText())){ // validation for not entering a project description
            JOptionPane.showMessageDialog(null, "Enter a Project Description.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtDescription.requestFocusInWindow();
        }
        else if ("".equals(txtStartDate.getText())){ // validation for not entering start date
            JOptionPane.showMessageDialog(null, "Enter a Start Date first.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtStartDate.requestFocusInWindow();
        }
        else if(!Project.validDateFormat(txtStartDate.getText())){ // validation for correct format of start date entered
            JOptionPane.showMessageDialog(null, "Start Date format is invalid.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtStartDate.setText("");  
            txtStartDate.requestFocusInWindow();
        }
        else if(!Project.validFutureDate(txtStartDate.getText())){ // validating that start date is in the future
            JOptionPane.showMessageDialog(null, "Start Date cannot be today or a past date.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtStartDate.setText("");  
            txtStartDate.requestFocusInWindow();
        }
        else if ("".equals(txtEndDate.getText())){ // validation for not entering end date
            JOptionPane.showMessageDialog(null, "Enter an End Date first.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtEndDate.requestFocusInWindow();
        }
        else if(!Project.validDateFormat(txtEndDate.getText())){ // validation for correct format of end date
            JOptionPane.showMessageDialog(null, "End Date format is invalid.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtEndDate.setText("");  
            txtEndDate.requestFocusInWindow();
        }
        else if(!Project.validFutureDate(txtEndDate.getText())){ // validating that end date is in the future
            JOptionPane.showMessageDialog(null, "End Date cannot be today or a past date.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtEndDate.setText("");  
            txtEndDate.requestFocusInWindow();
        }
        else if(!Project.endDateAfterStart(txtEndDate.getText(), txtStartDate.getText())){ // validating that end date is after start date
            JOptionPane.showMessageDialog(null, "End Date cannot be before Start Date.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtEndDate.setText("");  
            txtEndDate.requestFocusInWindow();
        }
        else if ("".equals(txtConstructionSite.getText())){ // validation for not entering construction site
            JOptionPane.showMessageDialog(null, "Enter the Construction Site Details first.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtConstructionSite.requestFocusInWindow();
        }
        else if ("".equals(txtContractor.getText())){ // validation for not entering contractor name
            JOptionPane.showMessageDialog(null, "Enter the Contractor first.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtContractor.requestFocusInWindow();
        }
        else if("".equals(txtBudget.getText())){ // validation for not entering budget
            JOptionPane.showMessageDialog(null, "Enter a Budget first.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtBudget.requestFocusInWindow();
        }
        else if (!Project.validBudget(txtBudget.getText())){ // validation for not entering budget in a double format
            JOptionPane.showMessageDialog(null, "Invalid Budget amount entered.", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtBudget.setText("");
            txtBudget.requestFocusInWindow();
        }
        else if (schedulerAssigned.isEmpty()){ // validation to check if staffs were assigned
            JOptionPane.showMessageDialog(null, "Add Staff to work on this project first.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            String status = createProject(); // creating project
            JOptionPane.showMessageDialog(null, status, "Information", JOptionPane.INFORMATION_MESSAGE);
            
            // clearing everything for next project created
            txtProjectName.setText("");
            txtDescription.setText("");
            txtStartDate.setText("");
            txtEndDate.setText("");
            txtConstructionSite.setText("");
            txtContractor.setText("");
            txtBudget.setText("");
            schedulerAssigned.clear();
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    // method to create project 
    private String createProject(){
        String projectID = Project.generateUniqueProjectID("ConstructionProjects.txt", "ConstructionProject"); // generating the unique project ID
        double amount = Double.parseDouble(txtBudget.getText()); // parsing the budget entered to double
    
        // creating new construction project constructor
        ConstructionProject newProject = new ConstructionProject(projectID, txtProjectName.getText(), txtDescription.getText(), txtStartDate.getText(), txtEndDate.getText(), amount, schedulerAssigned, txtConstructionSite.getText(), txtContractor.getText());
        ArrayList<ConstructionProject> projects = new ArrayList(); // creating ArrayList as writeToFile method take an ArrayList as paramtere
        projects.add(newProject); // adding the project to the ArrayList
        
        FileOperations file = new FileOperations();
        String status = file.writeToFile("ConstructionProjects.txt", projects); // writing the construction project to file
        
        return status; 
        
    }
    
    // method to check availability of staff
    private boolean checkAvailabilityOfStaff(Scheduler scheduler){
        FileOperations file = new FileOperations();
        ArrayList<Casting> array = file.readFile("ConstructionProjects.txt"); // reading the text file

        if (array == null || array.isEmpty()) { // if the file is empty - no projects yet - staff is available
            return true;
        }

        Casting casting = new Casting();
        ArrayList<Object> projects = casting.convertRecordsToObjects(array, "ConstructionProject");

        if (projects == null || projects.isEmpty()) { // if there are no constrcution projects - staff is available
            return true;
        }

        // adding ConstructionProject object to the ArrayList created
        ArrayList<ConstructionProject> constructionProjects = new ArrayList<>();
        for (Object obj : projects) {
            if (obj instanceof ConstructionProject constructionProject) {
                constructionProjects.add(constructionProject);
            } else {
                JOptionPane.showMessageDialog(null, "Unexpected object type: " + obj.getClass().getName(), "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }

        // looping through ConstructionProject object 
        // checking if scheduler object is in the ConstructionProject
        for (ConstructionProject project : constructionProjects) {
            if (project.getSchedulers().contains(scheduler)) {// if the project contains the scheduler object, returning false
                return false; 
            }
        }
        return true; 
    }
    
