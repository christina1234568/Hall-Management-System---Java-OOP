    private void cboSalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboSalesActionPerformed
        int i = cboSales.getSelectedIndex();
        File checkFile = new File("Payment.txt");
        if (!checkFile.exists()) { //checking if Payment.txt exists before reading it
            JOptionPane.showMessageDialog(null, "No sales have been made until now.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        else{ // Payment.txt exists
            FileOperations view = new FileOperations();
            ArrayList<Casting> paymentRecords = view.readFile("Payment.txt");//reading records in Payment.txt

            if (!paymentRecords.isEmpty()){ // checking if there are payments made yet
                Casting rec = new Casting();
                ArrayList<Object> records = rec.convertRecordsToObjects(paymentRecords,"Payment"); //casting method
                ArrayList<Payment> salesRecords = new ArrayList<>();

                // adding payment objects to the arraylist
                // validating if it is an instance of payment
                for (Object obj : records) {
                    if (obj instanceof Payment payment) {
                        salesRecords.add(payment);
                    } 
                    else {
                        JOptionPane.showMessageDialog(null, "Unexpected object type: " + obj.getClass().getName(), "Information", JOptionPane.INFORMATION_MESSAGE);
                    }
                }

                DefaultTableModel table = (DefaultTableModel) tblSales.getModel();
                table.setRowCount(0);//clearing table before adding new rows so that they do are not just appended again below existing rows

                // switching between types of sales obtained from cboSales
                switch (i){
                    case 0:
                    {
                        WeeklySales weekly = new WeeklySales();
                        ArrayList<WeeklySales> weeklyRecord = weekly.calculateAmounts(salesRecords); //calculating the weekly amounts with respective end and start dates
                        for (WeeklySales record : weeklyRecord){ // looping through arrayList created
                            table.addRow(new Object[]{ // creating the object using getters, and adding to the table
                                record.getStartDate(),
                                record.getEndDate(),
                                record.getAmount()
                            });
                        }
                        break;
                    }
                    case 1:
                    {
                        MonthlySales monthly = new MonthlySales();
                        ArrayList<MonthlySales> monthlyRecord = monthly.calculateAmounts(salesRecords);
                        for (MonthlySales record : monthlyRecord) {
                            table.addRow(new Object[]{
                                record.getStartDate(),
                                record.getEndDate(),
                                record.getAmount()
                            });
                        }
                        break;
                    }
                    case 2:
                    {
                        YearlySales yearly = new YearlySales();
                        ArrayList<YearlySales> yearlyRecord = yearly.calculateAmounts(salesRecords);
                        for (YearlySales record : yearlyRecord) {
                            table.addRow(new Object[]{
                                record.getStartDate(),
                                record.getEndDate(),
                                record.getAmount()
                            });
                        }
                        break;
                    }
                }//closing switch
            }
            else{
                JOptionPane.showMessageDialog(null, "No sales have been made until now.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_cboSalesActionPerformed

