    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        DefaultTableModel table = (DefaultTableModel) tblSales.getModel();
        if (table.getRowCount() > 0){ // if sales have been calculated and displayed
            String type = cboSales.getSelectedItem().toString(); //fetching the type to be saved
            FileOperations file = new FileOperations();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            
            // switching between types displayed in the cboSales
            switch(type){
                case "Weekly":
                {
                    // clearing the files before saving record as writeToFile() 
                    // will append all the displayed records to the existing file
                    // to prevent this resaving records that have already been saved -
                    // clearing the file before writing to file
                    FileOperations.clearFile("WeeklySales.txt");
                    ArrayList<WeeklySales> weekly = new ArrayList<>();
                    for (int row = 0; row < table.getRowCount(); row++) {
                        //parsing displayed dates to correct LocalDate format using formatter
                        LocalDate startDate = LocalDate.parse(table.getValueAt(row, 0).toString(), formatter);
                        LocalDate endDate = LocalDate.parse(table.getValueAt(row, 1).toString(), formatter);
                        double amount = Double.parseDouble(table.getValueAt(row, 2).toString());
                        WeeklySales weeklySale= new WeeklySales(startDate, endDate, amount);
                        weekly.add(weeklySale);
                    }
                    String status = file.writeToFile("WeeklySales.txt", weekly);//writing records to file
                    JOptionPane.showMessageDialog(null, status, "Information", JOptionPane.INFORMATION_MESSAGE);
                    break;
                }
                case "Monthly":
                {
                    FileOperations.clearFile("MonthlySales.txt");
                    ArrayList<MonthlySales> monthly = new ArrayList<>();
                    for (int row = 0; row < table.getRowCount(); row++) {
                        LocalDate startDate = LocalDate.parse(table.getValueAt(row, 0).toString(), formatter);
                        LocalDate endDate = LocalDate.parse(table.getValueAt(row, 1).toString(), formatter);
                        double amount = Double.parseDouble(table.getValueAt(row, 2).toString());
                        MonthlySales monthlySale= new MonthlySales(startDate, endDate, amount);
                        monthly.add(monthlySale);
                    }
                    String status = file.writeToFile("MonthlySales.txt", monthly);
                    JOptionPane.showMessageDialog(null, status, "Information", JOptionPane.INFORMATION_MESSAGE);
                    break;
                }
                case "Yearly":
                {
                    FileOperations.clearFile("YearlySales.txt");
                    ArrayList<YearlySales> yearly = new ArrayList<>();
                    for (int row = 0; row < table.getRowCount(); row++) {
                        LocalDate startDate = LocalDate.parse(table.getValueAt(row, 0).toString(), formatter);
                        LocalDate endDate = LocalDate.parse(table.getValueAt(row, 1).toString(), formatter);
                        double amount = Double.parseDouble(table.getValueAt(row, 2).toString());
                        YearlySales yearlySale= new YearlySales(startDate, endDate, amount);
                        yearly.add(yearlySale);
                    }
                    String status = file.writeToFile("YearlySales.txt", yearly);
                    JOptionPane.showMessageDialog(null, status, "Information", JOptionPane.INFORMATION_MESSAGE);
                    break;
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "No sales record has been generated yet.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

