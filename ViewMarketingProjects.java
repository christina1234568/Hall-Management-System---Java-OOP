    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        File checkFile = new File("MarketingProjects.txt");
        if (!checkFile.exists()) { //checking if file exists first
            JOptionPane.showMessageDialog(null, "No Marketing Project has been added yet.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            FileOperations file = new FileOperations();
            ArrayList<Casting> records = file.readFile("MarketingProjects.txt");
            if (records.isEmpty()){ //checking if file is empty first
                JOptionPane.showMessageDialog(null, "No Marketing Project has been added yet.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                Casting cast = new Casting();
                ArrayList<Object> projects = cast.convertRecordsToObjects(records, "MarketingProject");
                
                ArrayList<MarketingProject> marketingList = new ArrayList();
                //adding marketing project to the ArrayList
                for (Object obj : projects) {
                    if (obj instanceof MarketingProject marketingProject) {
                        marketingList.add(marketingProject);
                    } 
                    else {
                        JOptionPane.showMessageDialog(null, "Unexpected object type: " + obj.getClass().getName(), "Information", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
                
                
                
                DefaultTableModel table = (DefaultTableModel) tblMarketingProject.getModel();//getting the model of the table
                
                // looping theorugh the ArrayList
                for (MarketingProject i : marketingList) {
                    String schedulersString = i.getSchedulers().stream() // getting the Schedulers ArrayList in each marketing project object and converting into a stream
                           .map(Scheduler::toStringForProject)  // using the stream to convert each Scheduler to a readable format using .map and the toStringForProject() method
                           .collect(Collectors.joining(", ")); // joining the schedulers strings with a ", "
                    
                    // adding a new row with the current marketing objects attributes using getters and the schedulersString created
                    table.addRow(new Object[]{
                        i.getProjectID(),
                        i.getProjectName(),
                        i.getDescription(),
                        i.getStartDate(),
                        i.getEndDate(),
                        i.getBudget(),
                        schedulersString,
                        i.getTargetAudience()
                    });
                } 
            }
        }
    }//GEN-LAST:event_formWindowOpened

