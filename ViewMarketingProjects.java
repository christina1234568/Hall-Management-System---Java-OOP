    //viewing staff ID and staff name of staffs working on a selected project
    private void btnViewStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewStaffActionPerformed
        DefaultTableModel table = (DefaultTableModel) tblStaff.getModel();
        table.setRowCount(0);//clearing the table before displaying staff
        if (tblMarketingProject.getSelectedRow() != -1){
            String selectedProjectID = tblMarketingProject.getValueAt(tblMarketingProject.getSelectedRow(), 0).toString();//getting selected project ID
            FileOperations file = new FileOperations();
            ArrayList<Casting> records = file.searchRecord("MarketingProjects.txt", selectedProjectID, 0); // searching the record corresponding to the projectID
            Casting cast = new Casting();
            ArrayList<Object> projects = cast.convertRecordsToObjects(records, "MarketingProject"); 

            ArrayList<MarketingProject> marketingList = new ArrayList();
            // conerting to MarketingProject object
            for (Object obj : projects) {
                if (obj instanceof MarketingProject marketingProject) {
                    marketingList.add(marketingProject);
                } 
                else {
                    JOptionPane.showMessageDialog(null, "Unexpected object type: " + obj.getClass().getName(), "Information", JOptionPane.INFORMATION_MESSAGE);
                }
            }

            // adding the details in tblStaff
            for (MarketingProject project : marketingList){
                if (project.getProjectID().equals(selectedProjectID)){
                    ArrayList<Scheduler> staff = project.getSchedulers();
                    for (Scheduler i : staff){
                        table.addRow(new Object[]{
                            i.getId(),
                            i.getName()
                        });
                    }
                    break;
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Select a Project in the table first.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnViewStaffActionPerformed

